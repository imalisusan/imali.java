package javaapplication4;

public class JavaApplication4 {

    public static void main(String[] args) {
        int numberOne=23;
        int numberTwo=30;
        System.out.println("23+30 is: "+(numberOne+numberTwo));
        System.out.println("23-30 is: "+(numberOne-numberTwo));
        System.out.println("23*30 is: "+(numberOne*numberTwo));
        System.out.println("23/30 is: "+(numberOne/numberTwo));
        System.out.println("23%30 is: "+(numberOne%numberTwo));
    }
    
}
